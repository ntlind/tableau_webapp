import type { NextPage } from 'next'
import Head from 'next/head'
import { useEffect, useState, useRef } from 'react';
// @ts-ignore
import { DragDropContext, Droppable, Draggable } from "react-beautiful-dnd";
import { getIndexOfString, copy, category_names, getItems, reorder, fetchData, getTypeClassification } from '../components/DragAndDrop/DragAndDrop'


function QuoteApp() {
  let isInitialMount = useRef(true)
  const [state, setState] = useState([]); // has to start out empty or the droppable won't work
  const [data, setData] = useState({ data: null, cols: null, types: null, classifications: null }); // has to start out empty or the droppable won't work

  useEffect(() => {
    if (isInitialMount.current) {
      fetchData('https://raw.githubusercontent.com/facebook/prophet/main/examples/example_retail_sales.csv', setData)
      isInitialMount.current = false
    }

    if ((data.data !== null) && (state.length === 0)) {
      let metric_array = Array(data.cols![getIndexOfString(data.classifications!, 'metric')])
      let dimension_array = Array(data.cols![getIndexOfString(data.classifications!, 'dimension')])
      // @ts-ignore
      setState([...state, getItems(metric_array), getItems(dimension_array), [], [], []])
    }

  }, [data, state])

  function onDragEndDuplicate(result: any) {
    const { source, destination } = result;

    // dropped outside the list
    if (!destination) {
      return;
    }
    const sInd = +source.droppableId;
    const dInd = +destination.droppableId;

    if (sInd === dInd) {
      const items = reorder(state[sInd], source.index, destination.index);
      const newState: any = [...state];
      newState[sInd] = items;
      setState(newState);
    } else {
      let result: any = copy(state[sInd], state[dInd], source, destination);
      const newState: any = [...state];
      newState[sInd] = result[sInd];
      newState[dInd] = result[dInd];

      setState(newState);
    }
  }

  return (
    <div className='w-60 fixed left-0 h-screen border-r border-gray-200 z-1 shadow-lg'>
      <div className='flex flex-col'>
        <DragDropContext onDragEnd={onDragEndDuplicate}>
          {state.map((el, ind) => (
            <Droppable key={ind} droppableId={`${ind}`} isDropDisabled={ind >= 2 ? false : true}>
              {(provided, snapshot) => (
                <div
                  ref={provided.innerRef}
                  className={snapshot.isDraggingOver ? "bg-blue-50 p-4 pb-6" : "bg-white p-4 pb-6"}
                  {...provided.droppableProps}
                >
                  <div className={ind == 2 ? "border-t-2 border-gray-200 pt-6" : ""}></div>
                  <div className='font-bold underline'>
                    {category_names[ind]}
                  </div>
                  {el.map((item, index) => (
                    <Draggable
                      key={item.id}
                      draggableId={item.id}
                      index={index}
                    >
                      {(provided, snapshot) => (
                        <div
                          ref={provided.innerRef}
                          {...provided.draggableProps}
                          {...provided.dragHandleProps}
                          className={snapshot.isDragging ? "bg-blue-900 text-white select-none p-2 ml-4 rounded-xl" : "bg-white select-none p-2 ml-4"}
                        >
                          <div>
                            {item.content}
                          </div>
                        </div>
                      )}
                    </Draggable>
                  ))}
                  {provided.placeholder}
                </div>
              )}
            </Droppable>
          ))}
          <button className='absolute z-10 theme-button rounded left-[4.5rem] bottom-6 w-24' onClick={(e) => setState([state[0], state[1], [], [], []])}>Reset</button>
        </DragDropContext>
      </div>
    </div >
  );
}

const Home: NextPage = () => {
  return (
    <div >
      <Head>
        <title>Tableau Webapp</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main>
        <div className='w-screen-1/2'><QuoteApp /></div>

      </main>

      <footer>
        blah
      </footer>
    </div>
  )
}

export default Home
